    private void RetrieveVersion() {
        try (Statement stmt = connection.createStatement()) {
            ResultSet rs = stmt.executeQuery("SELECT * FROM tblversion");
            if (rs.next()) {
                lblversion.setText(rs.getString("Version"));
                lblversion2.setText(rs.getString("Version") + "Release Notes:");
                lblinfodetails.setText(rs.getString("Info"));
                lblbugfix.setText(rs.getString("BugFixes"));
                String feature = rs.getString("Features");
                String[] featureline = feature.split(",");
//                lblfeature.setText(featureline[0]);
//                lblfeature1.setText(featureline[1]);
//                lblfeature2.setText(featureline[2]);
                for (int i = 0; i < featureline.length; i++) {
                    lblfeature.setText(featureline[i]);
                }

            }
            stmt.close();
        } catch (SQLException e) {
        }
    }

