    private void RetrieveData() {
        DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
        while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
        }
        try (Statement stmt = connection.createStatement()) {
            ResultSet rs = stmt.executeQuery("SELECT * FROM tbluser");
            while (rs.next()) {
                String sid = rs.getString("id");
                String stype = rs.getString("usertype");
                String username = rs.getString("username");
                String password = rs.getString("password");
                tableModel.addRow(new Object[]{sid, stype, username, password});
            }
        } catch (SQLException e) {
        }
    }

    private void Delete() {
        int row = table.getSelectedRow();
        if (row > -1) {
            if (JOptionPane.showConfirmDialog(this, "Are you sure you want to delete user '" + txtusername.getText() + "'?", " System Information", JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                String sid = table.getValueAt(row, 0).toString();
                String sqlc = "DELETE FROM tbluser WHERE ID = ?";
                try (PreparedStatement stmt = connection.prepareStatement(sqlc)) {
                    stmt.setString(1, sid);
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "User '" + txtusername.getText() + "' has been deleted!", " System Information", JOptionPane.INFORMATION_MESSAGE);
                    refresh();
                } catch (SQLException ex) {
                    Logger.getLogger(FrameUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select data.", " System Information", JOptionPane.WARNING_MESSAGE);
        }
    }

